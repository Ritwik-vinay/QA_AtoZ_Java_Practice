package Java_F_Task;

public class class62_Arrays {
    public static void main(String[] args) {
        //basic Arrays
        int a[] = new int[5];
        a[0] = 5;
        a[1] = 4;
        a[2] = 3;
        a[3] = 2;
        a[4] = 1;

        for (int i = 1; i <= a.length; i++) {
            System.out.println(i);
        }
    }
}
