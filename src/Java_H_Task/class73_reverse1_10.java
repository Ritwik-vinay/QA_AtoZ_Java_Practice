package Java_H_Task;

public class class73_reverse1_10 {
    public static void main(String[] args) {
        // Print Numbers from 10 to 1 (Reverse Order)
        //i<=1; i=10
        for (int i = 10; i >= 1; i--) {
            System.out.println(i);
        }
        System.out.println("***************");
        int n = 10;
        while (n >= 1) {
            System.out.println(n);
            n--;
        }

    }
}
