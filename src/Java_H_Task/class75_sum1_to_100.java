package Java_H_Task;

public class class75_sum1_to_100 {
    public static void main(String[] args) {
        int c = 0;

        for (int i = 1; i <= 100; i++) {
            c += i;

        }
        System.out.println(c);
        int sum = 0;
        int j = 1;
        while (j <= 100) {
            sum += j;
            j++;


        }
        System.out.println(sum);

    }
}
